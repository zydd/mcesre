# dragon
1$dra=[$1?1l8$1-1$dra!|1r8$1-1$drai!b 1r8  {>} C >>{>v}Cvv {v}]
1$drai=[$1?1r8$1-1$dra!|1l8$1-1$drai!b 1r8 {v} C vv{v>}C>> {>}]

# c
1$c=[$1?$1-1$c!1r4,$1-1$c!b {>}C >> {>v}C vv {v}]
1$ci=[$1?1r8$1-1$ci!|7r8$1-1$ci!b 1z4 1r8> >>>vvvl v]

# hilbert
1$hil=[$1?$1-1[3r4 0-1y$hil! | ^ $hil! | > $hil! | v 1r4 0-1y$hil!]b ^>v]

# snowflake
1$goss=[?$1-1[1r6 1z2 $1$goss! 5r6 $1$goss! 4r6 $1$1$goss!$goss! 2r6 $1$goss! 1r6 $1$goss!]b $goss0!]
$goss0=[1r6 1z2 >  5r6 >  4r6 >>    2r6 >  1r6 > ]

# gos
1$gos=,[$1?$1-1[    $2$gos! 1r6   $2$gosi!      2r6 $2$gosi!  5r6 $2$gos!  4r6 $2$gos!$2$gos! 5r6 $2$gosi!]b $gos0!]
1$gosi=[$1?$1-1[5r6 $2$gos! 1r6 $2$gosi!$2$gosi!  2r6 $2$gosi!  1r6 $2$gos!  4r6   $2$gos!    5r6 $2$gosi!]b $gosi0!]
$gos0=,[2z5     > 1r6  >  2r6 >  5r6 >  4r6 >>  5r6 >]
$gosi0=[2z5 5r6 > 1r6 >>  2r6 >  1r6 >  4r6 >   5r6 >]

# z
1$z=[$1?[$1-1[$1$z! {$1$len! y>^} $1$z! {$2$len! x<} {v} $1$z! {$1$len! y>^} $1$z! ]b] C >{<v}C>]
1$len=2$1p*2-1
